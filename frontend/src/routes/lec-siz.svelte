    <div class="content-section">
      <h2>7. Экологические аспекты и утилизация СИЗ</h2>
      <p>СИЗ — это не просто инструмент безопасности, но и источник отходов. Многие материалы (резина, полипропилен, силикон, фильтры) не разлагаются и загрязняют окружающую среду.</p>

      <h3>7.1. Проблемы</h3>
      <ul>
        <li>Ежегодно в РФ утилизируется более 1,2 млрд единиц одноразовых СИЗ (маски, перчатки, респираторы)</li>
        <li>Более 80% — попадают в обычные контейнеры, а не в специализированные пункты</li>
        <li>Фильтры респираторов содержат тяжёлые металлы и химикаты</li>
      </ul>

      <h3>7.2. Рекомендации по утилизации</h3>
      <ul>
        <li>Одноразовые СИЗ — сдавать в специальные контейнеры для опасных отходов (класс 4–5)</li>
        <li>Многоразовые СИЗ — ремонтировать, а не выбрасывать</li>
        <li>Фильтры — утилизировать как химические отходы</li>
        <li>Сотрудничать с сертифицированными операторами по обращению с отходами (согласно ФЗ-89 «Об отходах производства и потребления»)</li>
        <li>Внедрять программы по переработке: например, переработка резиновых перчаток в дорожное покрытие</li>
      </ul>

      <div class="warning">
        <strong>Кейс: Компания «Эко-Производство»</strong><br>
        Внедрила систему сбора использованных респираторов и перчаток. Партнёр — компания «Эко-Сервис» (лидер в переработке СИЗ). Результат: утилизация 95% отходов, снижение штрафов на 100%, получение сертификата «Зелёный бизнес», снижение затрат на закупку новых СИЗ на 18% за счёт рециклинга.
      </div>
