      <p><strong>Средства индивидуальной защиты (СИЗ)</strong> — это технические устройства, предназначенные для защиты работника от воздействия опасных и вредных производственных факторов, таких как: шум, пыль, химические вещества, электрический ток, радиация, высокая/низкая температура, механические воздействия, биологические агенты и др.</p>
