      <h2>4. Психологические и организационные барьеры в использовании СИЗ</h2>
      <p>Технически правильный выбор СИЗ — это только половина успеха. Часто основной причиной неиспользования СИЗ являются <strong>психологические и культурные факторы</strong>.</p>

      <h3>4.1. Основные барьеры</h3>
      <ul>
        <li><strong>Комфорт</strong>: перегрев, давление на лицо, затруднённое дыхание — работники снимают СИЗ «на минутку».</li>
        <li><strong>Непонимание риска</strong>: «Я работал 10 лет без маски — ничего не было».</li>
        <li><strong>Социальное давление</strong>: «Все снимают — и я сниму».</li>
        <li><strong>Формализм</strong>: инструктажи проходят «для галочки» — нет практического обучения.</li>
        <li><strong>Отсутствие контроля</strong>: если никто не проверяет — СИЗ не используют.</li>
      </ul>

      <h3>4.2. Как преодолеть барьеры?</h3>
      <ul>
        <li>Проводить <strong>демонстрационные тренинги</strong> с реальными примерами (видео, симуляции).</li>
        <li>Внедрять <strong>систему мотивации</strong>: бонусы за соблюдение, «Капитан безопасности».</li>
        <li>Использовать <strong>«дружественные» СИЗ</strong>: лёгкие, с регулировкой, с вентиляцией, с возможностью носить очки под маской.</li>
        <li>Внедрять <strong>систему обратной связи</strong>: анонимные опросы о неудобствах СИЗ.</li>
        <li>Создавать <strong>культуру безопасности</strong>: руководители должны носить СИЗ первыми.</li>
      </ul>

