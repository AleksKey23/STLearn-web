      <h2>3. Методика выбора СИЗ: от оценки рисков до подбора</h2>
      <p>Выбор СИЗ — это научно обоснованный процесс, основанный на результатах оценки условий труда (ОУТ) и анализе производственных факторов.</p>

      <h3>3.1. Алгоритм выбора СИЗ</h3>
      <div class="algorithm">
        <h4>Шаг 1: Идентификация опасных факторов</h4>
        <p>Провести аудит рабочего места: шум, пыль, химикаты, температура, вибрация, электричество, падение с высоты и т.д.</p>

        <h4>Шаг 2: Классификация по степени вредности</h4>
        <p>Определить класс условий труда по результатам ОУТ: 1 — оптимальные, 2 — допустимые, 3 — вредные, 4 — опасные. СИЗ выдаются при классах 3 и 4.</p>

        <h4>Шаг 3: Определение параметров воздействия</h4>
        <p>Измерить концентрацию вредных веществ, уровень шума, интенсивность излучения. Сравнить с ПДК, ПДУ, ПДК(м.р.).</p>

        <h4>Шаг 4: Подбор по ГОСТ и Приказу № 308н</h4>
        <p>Сопоставить параметры воздействия с таблицами соответствия в ГОСТ и нормах выдачи. Например: концентрация пыли > 10 ПДК → респиратор FFP3.</p>

        <h4>Шаг 5: Проверка совместимости и эргономики</h4>
        <p>Проверить, не мешают ли СИЗ друг другу (например, респиратор и защитные очки). Провести примерку.</p>

        <h4>Шаг 6: Инструктаж и фиксация</h4>
        <p>Провести инструктаж по применению, проверить понимание, зафиксировать выдачу в журнале.</p>
      </div>

      <h3>3.2. Пример расчёта: выбор респиратора при сварке</h3>
      <p>На сварочном участке выявлено: концентрация сварочной пыли — 15 ПДК, оксиды азота — 0,8 ПДК, озон — 0,5 ПДК.</p>
      <p>Согласно ГОСТ 12.4.244-2019 и Приказу № 308н:</p>
      <ul>
        <li>Пыль больше 10 ПДК → требуется респиратор класса FFP3 (фильтр P3)</li>
        <li>Оксиды азота и озон — газы → требуются фильтры типа A (для кислотных газов)</li>
