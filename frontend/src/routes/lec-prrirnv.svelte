      <h2>2. Требования к грузам, таре и механизмам</h2>
      
      <h3>2.1 Грузы и тара: классификация и маркировка</h3>
      <p>Грузы классифицируются по:</p>
      <ul>
        <li><strong>Физическим свойствам:</strong> тяжёлые, хрупкие, опасные, сыпучие, жидкие</li>
        <li><strong>Форме и размерам:</strong> контейнеры, бочки, ящики, паллеты, рулоны</li>
        <li><strong>Опасности:</strong> согласно ГОСТ 19433-88 — классы опасных грузов (взрывоопасные, токсичные, коррозионные и т.д.)</li>
      </ul>

      <p>Тара должна иметь следующую маркировку (ГОСТ 14192-96):</p>
      <ul>
        <li>Масса брутто (общая) и нетто (чистая)</li>
        <li>Номер партии и дата производства</li>
        <li>Направление подъёма (стрелка)</li>
        <li>Надписи: «Хрупкий», «Не класть на бок», «Осторожно — вода»</li>
        <li>Штрих-код и идентификатор груза (для автоматизированных складов)</li>
      </ul>

      <h3>2.2 Стропы, канаты, крюки и приспособления</h3>
      <p>Стропы — один из самых уязвимых элементов в ПРР. Их необходимо проверять перед каждой сменой.</p>

      <div class="algorithm">
        <h4>Алгоритм проверки стропа перед использованием:</h4>
        <ol>
          <li>Проверить наличие бирки с датой последнего осмотра и грузоподъёмностью</li>
          <li>Осмотреть на наличие порезов, перетираний, обрывов нитей (особенно в местах соприкосновения с грузом)</li>
          <li>Проверить целостность застёжек, карабинов, петель — нет ли трещин, коррозии, деформации</li>
          <li>Протереть строп сухой тряпкой — если обнаружены волокна или пыль — это признак износа</li>
          <li>Если строп не имеет бирки — немедленно изъять из эксплуатации</li>
          <li>Занести результат осмотра в журнал технического обслуживания</li>
        </ol>
      </div>

      <p>Угол между ветвями стропа должен быть не более 90°. При угле 120° нагрузка на каждую ветвь увеличивается на 50%!</p>

      <h3>2.3 Механические средства: автопогрузчики, краны, тельферы</h3>
      <p>Обязательные требования к технике:</p>
      <ul>
        <li>Технический осмотр — не реже 1 раза в месяц (для погрузчиков) и 1 раз в квартал (для кранов)</li>
        <li>Регистрация в Ростехнадзоре (для кранов и подъёмников)</li>
        <li>Наличие паспорта, инструкции по эксплуатации и журнала осмотров</li>
        <li>Обязательное наличие аварийного стоп-кнопки, звукового сигнала, фонарей, зеркал заднего вида</li>
        <li>Оператор должен иметь удостоверение (квалификационная группа III)</li>
      </ul>

      <div class="important-note">
