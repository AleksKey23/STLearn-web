      <h2>7. Анализ реальных инцидентов и профилактика</h2>
      
      <h3>7.1 Кейс 1: Падение груза на складе</h3>
      <p><strong>Ситуация:</strong> На складе погрузчик поднял паллету с тяжёлыми бочками. Строп сорвался из-за перетирания. Груз упал на работающего, который находился в зоне безопасности — но не визуально контролировал её.</p>
      
      <p><strong>Причины:</strong></p>
      <ul>
        <li>Отсутствие ежедневного осмотра стропов</li>
        <li>Отсутствие ограждения зоны — только лента</li>
        <li>Работник не проходил инструктаж по ПРР 8 месяцев</li>
        <li>Нет журнала осмотров</li>
      </ul>
      
      <p><strong>Результат:</strong> Травма — перелом ребра. Штраф организации — 450 000 ₽. Руководитель — предупреждение и обязательный курс по ОТ.</p>

      <h3>7.2 Кейс 2: Падение с крыши</h3>
      <p><strong>Ситуация:</strong> Рабочий заменял кровельный материал на высоте 5 м. Не использовал страховочный пояс, так как «быстро справится». Упал, получив перелом позвоночника.</p>
      
      <p><strong>Причины:</strong></p>
      <ul>
        <li>Нет наряда-допуска</li>
        <li>Отсутствие проверки знаний у рабочего</li>
        <li>Руководитель знал о нарушении, но не предпринял действий</li>
        <li>СИЗ хранились в шкафу, но не выдавались</li>
      </ul>
      
      <p><strong>Результат:</strong> Рабочий — инвалид. Руководитель — уголовная ответственность по ст. 143 УК РФ (нарушение правил охраны труда, повлекшее по неосторожности тяжкий вред здоровью).</p>

      <h3>7.3 Профилактика: 5 ключевых шагов</h3>
      <ol>
        <li><strong>Каждый день — проверка СИЗ и оборудования</strong> — не «раз в неделю».</li>
        <li><strong>Зона безопасности — всегда ограждена</strong> — даже если «никто не ходит».</li>
        <li><strong>Наряд-допуск — не бумажка</strong> — это ваша защита от штрафов и трагедий.</li>
        <li><strong>Инструктаж — не «просто подписать»</strong> — объясняйте, почему это важно.</li>
        <li><strong>Ответственность — не уходит вверх</strong> — если вы руководитель, вы несёте ответственность за каждого сотрудника.</li>
      </ol>
