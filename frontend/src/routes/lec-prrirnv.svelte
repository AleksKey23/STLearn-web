      <h3>6.2 Проверка знаний и аттестация</h3>
      <p>Проверка знаний проводится комиссией в составе:</p>
      <ul>
        <li>Руководитель организации</li>
        <li>Специалист по охране труда</li>
        <li>Представитель профсоюза (по желанию)</li>
      </ul>

      <p>Формат проверки:</p>
      <ul>
        <li>Тестирование (20 вопросов, 80% правильных ответов — проходной балл)</li>
        <li>Практическая проверка: надевание СИЗ, проверка стропа, демонстрация действий при аварии</li>
        <li>Устный опрос по нормативным документам</li>
      </ul>

      <p>Результаты оформляются в протоколе, который хранится 5 лет. При неудовлетворительной оценке — повторная проверка через 14 дней. При повторном провале — отстранение от работы.</p>
