      <h2>5. Действия при аварийных ситуациях</h2>
      
      <h3>5.1 Падение груза</h3>
      <p>Алгоритм действий при падении груза:</p>

      <div class="algorithm">
        <h4>Алгоритм действий при падении груза:</h4>
        <ol>
          <li>Немедленно остановить все работы в зоне</li>
          <li>Сигнализировать об инциденте (свисток, звонок, радио)</li>
          <li>Оградить зону падения (ограждение, ленты, знаки)</li>
          <li>Не приближаться к грузу — он может быть нестабильным</li>
          <li>Проверить, нет ли повреждений коммуникаций (электричество, газ, вода)</li>
          <li>Вызвать ответственного за безопасность и аварийную службу</li>
          <li>Зарегистрировать инцидент в журнале учёта несчастных случаев</li>
          <li>Провести расследование в течение 3 дней</li>
        </ol>
      </div>

      <h3>5.2 Падение человека с высоты</h3>
      <p>Если человек упал — действия зависят от его состояния:</p>

      <div class="algorithm">
        <h4>Алгоритм оказания первой помощи при падении с высоты:</h4>
        <ol>
          <li>Не трогать пострадавшего — возможны переломы позвоночника, внутренние кровотечения</li>
          <li>Вызвать скорую помощь (103) и аварийную службу</li>
          <li>Установить ограждение, отвести людей — не допускать паники</li>
          <li>Если пострадавший в сознании — успокоить его, не давать пить и есть</li>
          <li>Если нет дыхания — начать СЛР (сердечно-лёгочную реанимацию)</li>
          <li>Если есть кровотечение — наложить давящую повязку</li>
          <li>Дождаться прибытия медиков — не перемещать пострадавшего</li>
        </ol>
      </div>

      <div class="warning">
        <strong>Важно:</strong> Даже если человек «встал сам», после падения с высоты требуется обязательное медицинское обследование — внутренние травмы могут проявиться через 12–48 часов.
      </div>

      <h3>5.3 Обрыв стропа или отказ механизма</h3>
      <p>При отказе оборудования:</p>
      <ul>
        <li>Немедленно остановить работу и отключить питание (если возможно)</li>
        <li>Эвакуировать персонал из опасной зоны</li>
        <li>Не пытаться ремонтировать самостоятельно — вызвать техническую службу</li>
        <li>Сохранить оборудование в неизменном виде для расследования</li>
        <li>Заполнить акт о технической неисправности</li>
      </ul>

      <p>Согласно ГОСТ 12.3.047-2022, все отказы оборудования регистрируются в журнале технических нарушений и рассматриваются как потенциальные причины аварий.</p>
    </div>

