      <h3>1.1 Общие требования и нормативная база</h3>
      <p>Погрузочно-разгрузочные работы (ПРР) — это комплекс операций по перемещению, погрузке, выгрузке, складированию и транспортировке грузов. Согласно <strong>ГОСТ Р 12.3.047-2022</strong> и <strong>ПОТ РМ-012-2023</strong>, все ПРР должны быть организованы с учётом:</p>
      
      <ul>
        <li>Оценки рисков по методике Роструда (Методические рекомендации № 123/2022)</li>
        <li>Соблюдения требований по эргономике и физиологии труда</li>
        <li>Использования только исправного и сертифицированного оборудования</li>
        <li>Наличия инструкций по безопасному выполнению работ для каждого вида ПРР</li>
        <li>Обучения персонала не реже 1 раза в 12 месяцев</li>
      </ul>

