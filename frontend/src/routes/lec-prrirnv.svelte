      <h2>3. Работа на высоте: классификация и допуск</h2>
      
      <h3>3.1 Что считается работой на высоте?</h3>
      <p>Согласно <strong>ПОТ РМ-012-2023</strong>, работа на высоте — это любые работы, выполняемые на высоте <strong>1,8 м и более</strong> от уровня земли, пола, площадки, а также:</p>
      <ul>
        <li>На лестницах, стремянках, подмостях, площадках без ограждений</li>
        <li>На крыше, балконе, фасаде здания</li>
        <li>Внутри резервуаров, бункеров, трубопроводов при высоте более 1,8 м</li>
        <li>На конструкциях, не имеющих ограждений высотой 1,1 м</li>
      </ul>

      <p>Также к работам на высоте относятся работы на высоте менее 1,8 м, если есть риск падения на:</p>
      <ul>
        <li>Острые предметы (трубы, арматура, ножи)</li>
        <li>Машины, механизмы, движущиеся части</li>
        <li>Жидкости, химикаты, токсичные вещества</li>
        <li>Глубокие ямы, траншеи, колодцы</li>
      </ul>

      <h3>3.2 Допуск к работе на высоте: пошаговый алгоритм</h3>

      <div class="algorithm">
        <h4>Алгоритм получения допуска:</h4>
        <ol>
          <li>Руководитель назначает ответственного за безопасность (инженер по ОТ)</li>
          <li>Проводится предварительный осмотр места работ и составление плана</li>
          <li>Составляется наряд-допуск (форма по Приложению 2 к ПОТ РМ-012-2023)</li>
          <li>Исполнитель проходит инструктаж с подписью в журнале</li>
          <li>Проверяется исправность СИЗ (пояс, амортизатор, каска)</li>
          <li>Организуется зона безопасности и ограждение</li>
          <li>Проводится контрольный осмотр перед началом работ</li>
          <li>Только после этого — допуск к работе</li>
        </ol>
      </div>

      <div class="warning">
        <strong>Критическое правило:</strong> Допуск выдаётся только на один рабочий день. При смене смены — новый наряд. При изменении условий (ветер, дождь, температура ниже -20°C) — работа немедленно прекращается.
      </div>
    </div>

